"""Neural/advanced language model scoring for existing K4 candidates.

Instead of trying new cipher methods, this strategy re-scores candidates
generated by diverse transformation families using a comprehensive character-level
n-gram language model.  A smooth log-probability score provides a better
gradient toward English than sparse anchor matching alone.

Transformations evaluated:
  a. K4 raw (baseline)
  b. All 26 Caesar shifts
  c. Vigenère decryptions with known-plaintext-consistent keys (periods 1-50)
  d. Columnar transposition inversions (widths 2-20, identity + keyword perms)
  e. K4 reversed, every-other-letter, diagonal/spiral reads
  f. Rail fence inversions (rails 2-20)

Each candidate is scored with:
  1. N-gram language model score (bigram + trigram + quadgram log-probs)
  2. Anchor alignment score (from shared infrastructure)
  3. Combined score (weighted sum)
"""

