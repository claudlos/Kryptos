def _caesar_shift(text: str, shift: int) -> str:
    """Apply a Caesar shift (shift each letter by `shift` positions)."""
    result = []
    for c in text:
        if c in STANDARD_ALPHABET:
            idx = (STANDARD_ALPHABET.index(c) - shift) % 26
            result.append(STANDARD_ALPHABET[idx])
        else:
            result.append(c)
    return "".join(result)


def _rail_fence_decrypt(ciphertext: str, rails: int) -> str:
    """Decrypt a rail fence cipher with the given number of rails."""
    if rails < 2 or rails >= len(ciphertext):
        return ciphertext
    n = len(ciphertext)
    # Build the zigzag pattern
    pattern = [0] * n
    cycle = 2 * (rails - 1)
    for i in range(n):
        mod = i % cycle
        pattern[i] = mod if mod < rails else cycle - mod

    # Count chars per rail
    rail_lens = [0] * rails
    for r in pattern:
        rail_lens[r] += 1

    # Split ciphertext into rails
    rail_texts: list[str] = []
    pos = 0
    for length in rail_lens:
        rail_texts.append(ciphertext[pos:pos + length])
        pos += length

    # Read back in zigzag order
    rail_indices = [0] * rails
    plaintext = []
    for i in range(n):
        r = pattern[i]
        plaintext.append(rail_texts[r][rail_indices[r]])
        rail_indices[r] += 1

    return "".join(plaintext)


def _extract_every_nth(text: str, n: int, offset: int = 0) -> str:
    """Extract every nth character starting at offset."""
    return text[offset::n]


def _read_diagonal(text: str, width: int) -> str:
    """Read text laid out in a grid diagonally (top-left to bottom-right)."""
    if width < 2:
        return text
    rows = (len(text) + width - 1) // width
    grid = []
    for r in range(rows):
        row = text[r * width:(r + 1) * width]
        grid.append(row)

    result = []
    # Read diagonals: top-left to bottom-right
    for d in range(rows + width - 1):
        for r in range(rows):
            c = d - r
            if 0 <= c < width and r < len(grid) and c < len(grid[r]):
                result.append(grid[r][c])
    return "".join(result)


def _read_spiral(text: str, width: int) -> str:
    """Read text laid out in a grid in spiral order (outside-in, clockwise)."""
    if width < 2:
        return text
    rows = (len(text) + width - 1) // width
    # Build grid
    grid: list[list[str]] = []
    for r in range(rows):
        row_text = text[r * width:(r + 1) * width]
        grid.append(list(row_text))

    result = []
    top, bottom, left, right = 0, rows - 1, 0, width - 1

    while top <= bottom and left <= right:
        # Top row
        for c in range(left, right + 1):
            if top < len(grid) and c < len(grid[top]):
                result.append(grid[top][c])
        top += 1
        # Right column
        for r in range(top, bottom + 1):
            if r < len(grid) and right < len(grid[r]):
                result.append(grid[r][right])
        right -= 1
        # Bottom row
        if top <= bottom:
            for c in range(right, left - 1, -1):
                if bottom < len(grid) and c < len(grid[bottom]):
                    result.append(grid[bottom][c])
            bottom -= 1
        # Left column
        if left <= right:
            for r in range(bottom, top - 1, -1):
                if r < len(grid) and left < len(grid[r]):
                    result.append(grid[r][left])
            left += 1

    return "".join(result)


# ---------------------------------------------------------------------------
# Main strategy
# ---------------------------------------------------------------------------

def run(config: StrategyRuntimeConfig | None = None):
    config = config or StrategyRuntimeConfig()
    attempts = 0
    scored_candidates: list[tuple[float, str, str, dict[str, object]]] = []

    def _evaluate(text: str, label: str, key_material: dict[str, object] | None = None) -> None:
        nonlocal attempts
        attempts += 1
        if not text or len(text) < 4:
            return
        score = combined_score(text)
        scored_candidates.append((score, text, label, key_material or {}))

    # -----------------------------------------------------------------------
    # (a) K4 raw baseline
    # -----------------------------------------------------------------------
    _evaluate(K4, "raw")

    # -----------------------------------------------------------------------
    # (b) All 26 Caesar shifts
    # -----------------------------------------------------------------------
    for shift in range(26):
        decrypted = _caesar_shift(K4, shift)
        _evaluate(decrypted, f"caesar:shift={shift}", {"shift": shift})

    # -----------------------------------------------------------------------
    # (c) Vigenère with known-plaintext-consistent keys (periods 1-50)
    # -----------------------------------------------------------------------
    for period in range(1, 51):
        key = infer_repeating_vigenere_key(K4, period)
        if key is not None:
            decrypted = decrypt_vigenere_standard(K4, key)
            _evaluate(
                decrypted,
                f"vigenere:period={period}:key={key}",
                {"period": period, "key": key},
            )

    # -----------------------------------------------------------------------
    # (d) Columnar transposition inversions (widths 2-20)
    # -----------------------------------------------------------------------
    for width in range(2, 21):
        # Identity permutation
        id_perm = identity_permutation(width)
        for fill_mode, read_mode in (("row", "column"), ("column", "row")):
            try:
                decrypted = periodic_transposition_decrypt(
                    K4, width, id_perm,
                    fill_mode=fill_mode, read_mode=read_mode,
                )
                _evaluate(
                    decrypted,
                    f"transposition:w={width}:id:{fill_mode}->{read_mode}",
                    {"width": width, "permutation": "identity", "fill": fill_mode, "read": read_mode},
                )
            except Exception:
                pass

        # Keyword permutations
        for kw in DEFAULT_KEYWORDS:
            try:
                kw_perm = keyword_permutation(kw, width)
                for fill_mode, read_mode in (("row", "column"), ("column", "row")):
                    decrypted = periodic_transposition_decrypt(
                        K4, width, kw_perm,
                        fill_mode=fill_mode, read_mode=read_mode,
                    )
                    _evaluate(
                        decrypted,
                        f"transposition:w={width}:{kw}:{fill_mode}->{read_mode}",
                        {"width": width, "keyword": kw, "fill": fill_mode, "read": read_mode},
                    )
            except Exception:
                pass

        # Reversed variants (identity only to keep count manageable)
        for reverse_rows, reverse_columns in ((True, False), (False, True), (True, True)):
            try:
                decrypted = periodic_transposition_decrypt(
                    K4, width, id_perm,
                    fill_mode="row", read_mode="column",
                    reverse_rows=reverse_rows, reverse_columns=reverse_columns,
                )
                _evaluate(
                    decrypted,
                    f"transposition:w={width}:rev_r={reverse_rows}:rev_c={reverse_columns}",
                    {"width": width, "reverse_rows": reverse_rows, "reverse_columns": reverse_columns},
                )
            except Exception:
                pass

    # -----------------------------------------------------------------------
    # (e) Pattern reads: reversed, every-other, diagonal, spiral
    # -----------------------------------------------------------------------
    # Reversed
    _evaluate(K4[::-1], "reversed")

    # Every-other-letter extraction (even/odd positions)
    for offset in (0, 1):
        extracted = _extract_every_nth(K4, 2, offset)
        _evaluate(extracted, f"every_other:offset={offset}", {"offset": offset})

    # Every-3rd-letter
    for offset in range(3):
        extracted = _extract_every_nth(K4, 3, offset)
        _evaluate(extracted, f"every_third:offset={offset}", {"offset": offset})

    # Interleave halves
    half = len(K4) // 2
    first_half = K4[:half]
    second_half = K4[half:]
    interleaved = "".join(
        a + b for a, b in zip(first_half, second_half)
    ) + (first_half[len(second_half):] if len(first_half) > len(second_half) else second_half[len(first_half):])
    _evaluate(interleaved, "interleave_halves")

    # Reversed halves concatenated
    _evaluate(first_half[::-1] + second_half[::-1], "reversed_halves")
    _evaluate(second_half + first_half, "swapped_halves")

    # Diagonal reads at various grid widths
    for width in (7, 8, 9, 10, 11, 12, 13, 14):
        diag = _read_diagonal(K4, width)
        _evaluate(diag, f"diagonal:w={width}", {"width": width})

    # Spiral reads at various grid widths
    for width in (7, 8, 9, 10, 11, 12, 13, 14):
        spiral = _read_spiral(K4, width)
        _evaluate(spiral, f"spiral:w={width}", {"width": width})

    # -----------------------------------------------------------------------
    # (f) Rail fence inversions (rails 2-20)
    # -----------------------------------------------------------------------
    for rails in range(2, 21):
        decrypted = _rail_fence_decrypt(K4, rails)
        _evaluate(
            decrypted,
            f"railfence:rails={rails}",
            {"rails": rails},
        )

    # -----------------------------------------------------------------------
    # Rank all candidates and retain top results
    # -----------------------------------------------------------------------
    # Sort by combined score descending
    scored_candidates.sort(key=lambda x: x[0], reverse=True)

    # Build ranked candidate objects for the top N
    retain_count = max(config.candidate_limit, 12)
    candidates: list[dict[str, object]] = []

    seen_previews: set[str] = set()
    for score_val, text, label, key_mat in scored_candidates:
        # Quick dedup on preview text
        preview = text[:72]
        if preview in seen_previews:
            continue
        seen_previews.add(preview)

        lang_score = ngram_language_score(text)
        anchor_score = anchor_alignment_score(text)

        candidate = build_ranked_candidate(
            text,
            transform_chain=[f"neural_scoring:{label}"],
            corpus_bundle=config.corpora,
            scorer_profile=config.scorer_profile,
            key_material={
                **key_mat,
                "neural_lang_score": round(lang_score, 2),
                "neural_anchor_score": anchor_score,
                "neural_combined": round(score_val, 2),
            },
        )
        candidates.append(candidate)
        if len(candidates) >= retain_count:
            break

    ranked = dedupe_ranked_candidates(candidates)
    retained = ranked[:retain_count]

    # Compute summary stats
    n_caesar = 26
    n_vigenere = sum(1 for p in range(1, 51) if infer_repeating_vigenere_key(K4, p) is not None)
    n_transposition = sum(1 for _ in range(2, 21)) * (2 + len(DEFAULT_KEYWORDS) * 2 + 3)
    n_pattern = 2 + 3 + 3 + 3 + len(range(7, 15)) * 2  # reversed, every-other, every-3rd, halves, diag+spiral
    n_railfence = 19

    best_lang = scored_candidates[0][0] if scored_candidates else 0.0
    best_text_preview = scored_candidates[0][1][:60] if scored_candidates else "N/A"

    return build_strategy_result(
        SPEC,
        retained,
        attempts=attempts,
        notes=[
            f"Evaluated {attempts} total candidates with comprehensive n-gram language model.",
            f"Caesar shifts: {n_caesar}, Vigenère keys: {n_vigenere}, "
            f"Transposition inversions: ~{n_transposition}, Pattern reads: ~{n_pattern}, "
            f"Rail fence: {n_railfence}.",
            f"Language model: {len(BIGRAM_LOG)} bigrams, {len(TRIGRAM_LOG)} trigrams, "
            f"{len(QUADGRAM_LOG)} quadgrams (log10 probabilities).",
            f"Scoring: {BIGRAM_WEIGHT:.0%} bigram + {TRIGRAM_WEIGHT:.0%} trigram + "
            f"{QUADGRAM_WEIGHT:.0%} quadgram, combined {LANG_MODEL_WEIGHT:.0%} language + "
            f"{ANCHOR_MODEL_WEIGHT:.0%} anchor.",
            f"Best combined neural score: {best_lang:.2f} ({best_text_preview}...).",
            "Key insight: smooth n-gram gradient provides finer discrimination than "
            "sparse anchor matching for 'almost English' candidates.",
        ],
    )
