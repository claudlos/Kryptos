def ngram_language_score(text: str) -> float:
    """Compute a comprehensive n-gram language score for the given text.

    Returns a score in [0, 1000] where higher = more English-like.
    Uses bigram, trigram, and quadgram log-probabilities normalized by text length.
    """
    if len(text) < 4:
        return 0.0

    upper = text.upper()

    # Bigram score
    bi_total = 0.0
    bi_count = 0
    for i in range(len(upper) - 1):
        bg = upper[i:i + 2]
        if bg[0] in STANDARD_ALPHABET and bg[1] in STANDARD_ALPHABET:
            bi_total += BIGRAM_LOG.get(bg, BIGRAM_FLOOR)
            bi_count += 1

    # Trigram score
    tri_total = 0.0
    tri_count = 0
    for i in range(len(upper) - 2):
        tg = upper[i:i + 3]
        if all(c in STANDARD_ALPHABET for c in tg):
            tri_total += TRIGRAM_LOG.get(tg, TRIGRAM_FLOOR)
            tri_count += 1

    # Quadgram score
    quad_total = 0.0
    quad_count = 0
    for i in range(len(upper) - 3):
        qg = upper[i:i + 4]
        if all(c in STANDARD_ALPHABET for c in qg):
            quad_total += QUADGRAM_LOG.get(qg, QUADGRAM_FLOOR)
            quad_count += 1

    # Normalize each by count
    bi_avg = bi_total / max(bi_count, 1)
    tri_avg = tri_total / max(tri_count, 1)
    quad_avg = quad_total / max(quad_count, 1)

    # Combined raw score (higher = better, all values are negative)
    raw = (
        BIGRAM_WEIGHT * bi_avg
        + TRIGRAM_WEIGHT * tri_avg
        + QUADGRAM_WEIGHT * quad_avg
    )

    # Map to [0, 1000]: typical English text scores ~-2.5 to -3.0 combined,
    # random text scores ~-5.5 to -6.5.
    # Scale: -2.0 -> 1000, -7.0 -> 0
    scaled = max(0.0, min(1000.0, (raw + 7.0) / 5.0 * 1000.0))
    return scaled


def combined_score(text: str) -> float:
    """Combined language model + anchor alignment score."""
    lang = ngram_language_score(text)
    anchor = float(anchor_alignment_score(text))
    # Anchor alignment score can be large; cap at 1000 for combination
    anchor_capped = min(anchor, 10000.0)
    # Normalize anchor to 0-1000 range (scores typically 0-10000)
    anchor_norm = min(1000.0, anchor_capped / 10.0)
    return LANG_MODEL_WEIGHT * lang + ANCHOR_MODEL_WEIGHT * anchor_norm


